// Constante para establecer la ruta y parámetros de comunicación con la API.
const API_EQUIPOS = '../../app/api/admin/equipos.php?action=';

document.addEventListener('DOMContentLoaded', function () {
    let today = new Date();
    let hour = today.getHours();
    let greeting = '';
    if (hour < 12) {
        greeting = 'Buenos días';
    } else if (hour < 19) {
        greeting = 'Buenas tardes';
    } else if (hour <= 23) {
        greeting = 'Buenas noches';
    }
    document.getElementById('greeting').textContent = greeting;
    graficaBarrasCondicion() 
    graficaBarrasTipo()
});

function graficaBarrasTipo() {
    fetch(API_EQUIPOS + 'cantidadEquiposTipo', {
        method: 'get'
    }).then(function (request) {
        // Se verifica si la petición es correcta, de lo contrario se muestra un mensaje indicando el problema.
        if (request.ok) {
            request.json().then(function (response) {
                // Se comprueba si la respuesta es satisfactoria, de lo contrario se remueve la etiqueta canvas de la gráfica.
                if (response.status) {
                    // Se declaran los arreglos para guardar los datos por gráficar.
                    let tipo = [];
                    let cantidad = [];
                    // Se recorre el conjunto de registros devuelto por la API (dataset) fila por fila a través del objeto row.
                    response.dataset.map(function (row) {
                        // Se asignan los datos a los arreglos.
                        tipo.push(row.tipoequipo);
                        cantidad.push(row.cantidad);
                    });
                    // Se llama a la función que genera y muestra una gráfica de barras. Se encuentra en el archivo components.js
                    barGraph('chart1', tipo, cantidad, 'Cantidad de Equipos', 'Cantidad de Equipos por Tipo');
                } else {
                    document.getElementById('chart1').remove();
                    console.log(response.exception);
                }
            });
        } else {
            console.log(request.status + ' ' + request.statusText);
        }
    }).catch(function (error) {
        console.log(error);
    });
}

